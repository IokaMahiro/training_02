# 味方NPCを導入すると、なぜRE4より面白くなるのか？を再考する

---

## 結論

> 味方NPCは不確定要素の意図的な混入として機能する。  
> RE4の広場戦の、攻略ルートの固定化という問題点を改善し、  
> 知識では消えない緊張感と、戦闘の選択肢をユーザーに与えるのではないか。

---

## 1. RE4の戦闘が抱える問題点

**攻略ルートの固定化**

様々な配置から逆算すると、最適解が必ず存在する。
周回するほどその解に収束し、意思決定の余地が少ない作業ゲーと化してしまう。

**緊張感の消費期限**

初周はガナードの多さ・リソースの少なさ・慣れない戦闘（村中央はチュートリアル後すぐに訪れる）・チェーンソーの出現など、一発ネタで面食らうイベントが連発する。
しかしその恐怖は初周だけ有効で、2周目以降はどんどん恐怖心が薄れてしまう。

**できることの上限の低さ**

ひとりである以上、ユーザーは全方位の脅威を自分で処理しなければならない。
これは難易度を生むが、行動の種類は増えない。射撃・逃げ・回復の3つが状況によって重み付けされるだけだと思う。

---

## 2. 味方NPCが加えるもの

### 不確定要素による攻略固定化の解消

ひとりの戦闘は「配置が固定・自分の行動が変数」という構造だ。味方NPCを加えると以下になる。

```
配置（固定）× 味方NPC（変数）× 自分の行動（変数）
```

変数の掛け合わせで状況が爆発的に増え、攻略の形骸化を防げるのではないか。

スレイザスパイアのようなローグライクが、ランダム生成で攻略の固定化を防ぐのと同じ原理で、繰り返し遊んでしまう「沼」としての面白さを加えられると思う。

### 選択肢の増加

| | 味方NPCなし | 味方NPCあり |
|---|---|---|
| 行動の種類 | 撃つ・逃げる・回復 | 上記＋挟撃と組技・囮にする・守る・逃走ルートの偶発的な発見 |
| 最適解の性質 | 固定 | 状況依存 |

選択肢が増えるだけでなく、戦闘の性質も変わる。
味方NPCがいないときは最適化するゲームで、味方NPCがいることで状況を読むゲームになるのではないか。

### 持続的な恐怖心と緊張感

味方NPCがいることで孤独でなくなり、怖くなくなるのでは、という意見に反論したい。

広場戦の恐怖心と緊張感は2周目以降、知識によって消えてしまう。
しかしながら味方NPCへの責任感から来る緊張感は消えない。
「味方NPCが今どこにいるか」「味方NPCに敵が集中していないか」はリアルタイムの状況判断であり、何周しても知識で解決できない。

---

## 3. そもそも、広場戦の恐怖心とは？

広場戦の恐怖心とは、数の暴力に対して有効な手段が限られているから怖いのだと考える。

味方NPCがいることで「2人がかりでも対処できない敵の数」という設計が可能になり、絶望感のスケールはむしろ大きくできるのではないか。
味方NPCをいい感じに弱く調整することで、孤立無援という感覚は演出できると思う。

---

## 4. まとめ

| 観点 | 味方NPCなし | 味方NPCあり |
|---|---|---|
| 攻略の固定化 | 起きやすい | 変数増加で起きにくい |
| 緊張感の持続 | 知識で消える | リアルタイムな判断が必要で消えない |
| 行動の選択肢 | 3種（撃つ・避ける・回復） | 質的に拡張される |
| リソマネの深さ | プレイヤーだけで最適化する | 味方NPC分のコストが乗る |

> 味方NPCの導入は「難しくする」ことではなく「解けなくする」ことだと思う。  
> 解けないゲームだけが、プレイヤーを意思決定者であり続けさせるのではないか。

